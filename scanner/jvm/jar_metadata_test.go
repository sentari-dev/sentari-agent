// ---------------------------------------------------------------------------
// parsePomProperties — the line-level parser
// ---------------------------------------------------------------------------

func TestParsePomProperties_Happy(t *testing.T) {
	// Canonical shape written by maven-jar-plugin.  Comment lines start
	// with "#" and must be skipped; trailing whitespace around values
	// is preserved-then-trimmed by the parser (Maven's own writer
	// trims, but we've seen field values with trailing CRLF in the
	// wild when authors hand-edit).
	content := []byte(`#Generated by Maven
#Fri Apr 11 14:30:00 CEST 2025
version=1.4.2
groupId=org.example
artifactId=widget-core
`)
	got, err := parsePomProperties(content)
	if err != nil {
		t.Fatalf("parsePomProperties: %v", err)
	}
	if got.GroupID != "org.example" {
		t.Errorf("groupId: got %q want org.example", got.GroupID)
	}
	if got.ArtifactID != "widget-core" {
		t.Errorf("artifactId: got %q want widget-core", got.ArtifactID)
	}
	if got.Version != "1.4.2" {
		t.Errorf("version: got %q want 1.4.2", got.Version)
	}
}

func TestParsePomProperties_MissingRequiredField(t *testing.T) {
	// A pom.properties without groupId is unusable as an identity source;
	// the caller should fall through to the next extraction strategy.
	// Parser returns the partial object + no error; the caller decides
	// whether a partial pom.properties is acceptable.
	content := []byte("artifactId=x\nversion=1\n")
	got, err := parsePomProperties(content)
	if err != nil {
		t.Fatalf("parser should not fail on incomplete input: %v", err)
	}
	if got.GroupID != "" {
		t.Errorf("expected empty groupId on input without it, got %q", got.GroupID)
	}
	if got.ArtifactID != "x" || got.Version != "1" {
		t.Errorf("partial parse unexpected: %+v", got)
	}
}

func TestParsePomProperties_MalformedLines(t *testing.T) {
	// Lines without an '=' are skipped, not an error — treat them as
	// noise (accidental blank-line re-save by an editor, etc.).  Lines
	// that split correctly keep their values verbatim, so an '=' inside
	// a version (rare, but legal in exotic classifier schemes) survives.
	content := []byte("just-a-line-with-no-equals\ngroupId=a\nartifactId=b\nversion=1.0=RC1\n")
	got, err := parsePomProperties(content)
	if err != nil {
		t.Fatalf("parser should not fail on noise lines: %v", err)
	}
	if got.Version != "1.0=RC1" {
		t.Errorf("expected '=' preserved inside value, got %q", got.Version)
	}
}

func TestParsePomProperties_ExceedsSizeCap(t *testing.T) {
	// Defence against a malicious or corrupt pom.properties >64 KiB —
	// the parser must refuse rather than hang.  Use one line so we're
	// sure it's the byte count, not a line count, that triggers.
	big := []byte("version=" + strings.Repeat("x", maxPomPropertiesBytes+1))
	_, err := parsePomProperties(big)
	if err == nil {
		t.Fatalf("expected error on oversized input, got nil")
	}
}

// ---------------------------------------------------------------------------
// parseManifest — MANIFEST.MF continuation lines + OSGi headers
// ---------------------------------------------------------------------------

func TestParseManifest_OSGiHeadersWin(t *testing.T) {
	// When both OSGi Bundle-* and Implementation-* are present, OSGi
	// identity is preferred — OSGi bundles use Bundle-SymbolicName as
	// the canonical identifier and Implementation-Version tends to
	// lag or be absent.  The scanner surfaces this choice via the
	// Source field on the returned ManifestInfo.
	content := []byte("Manifest-Version: 1.0\r\n" +
		"Bundle-SymbolicName: org.eclipse.jdt.core\r\n" +
		"Bundle-Version: 3.35.0\r\n" +
		"Implementation-Title: Java Development Tools\r\n" +
		"Implementation-Version: 3.35.legacy\r\n" +
		"\r\n")
	got, err := parseManifest(content)
	if err != nil {
		t.Fatalf("parseManifest: %v", err)
	}
	if got.BundleSymbolicName != "org.eclipse.jdt.core" {
		t.Errorf("BundleSymbolicName: got %q", got.BundleSymbolicName)
	}
	if got.BundleVersion != "3.35.0" {
		t.Errorf("BundleVersion: got %q", got.BundleVersion)
	}
}

func TestParseManifest_ContinuationLines(t *testing.T) {
	// RFC 822 / JAR-spec continuation: any line starting with a single
	// space is a continuation of the previous line.  Manifest writers
	// wrap at 72 bytes routinely, so we MUST honour this or we'll
	// silently truncate long names.
	// Here the Bundle-SymbolicName spans three physical lines.
	content := []byte("Manifest-Version: 1.0\r\n" +
		"Bundle-SymbolicName: org.some.very.long.identifier.tha\r\n" +
		" t.exceeds.seventy.two.bytes.and.keeps.going.for.a.whil\r\n" +
		" e.longer.still\r\n" +
		"Bundle-Version: 1.0\r\n")
	got, err := parseManifest(content)
	if err != nil {
		t.Fatalf("parseManifest: %v", err)
	}
	want := "org.some.very.long.identifier.that.exceeds.seventy.two.bytes.and.keeps.going.for.a.while.longer.still"
	if got.BundleSymbolicName != want {
		t.Errorf("continuation join failed\n got: %q\nwant: %q", got.BundleSymbolicName, want)
	}
}

func TestParseManifest_LFOnly(t *testing.T) {
	// Some tools emit LF-only manifests (not spec-compliant, but common
	// enough that refusing them would be too strict).  Parser must
	// accept both CRLF and LF.
	content := []byte("Manifest-Version: 1.0\nImplementation-Title: Foo\nImplementation-Version: 2.0\n")
	got, err := parseManifest(content)
	if err != nil {
		t.Fatalf("parseManifest: %v", err)
	}
	if got.ImplementationTitle != "Foo" || got.ImplementationVersion != "2.0" {
		t.Errorf("LF-only parse failed: %+v", got)
	}
}

func TestParseManifest_ExceedsSizeCap(t *testing.T) {
	// A MANIFEST.MF larger than maxManifestBytes must fail rather than
	// consume unbounded memory.
	big := []byte("Manifest-Version: 1.0\r\nJunk: " + strings.Repeat("x", maxManifestBytes+1) + "\r\n")
	_, err := parseManifest(big)
	if err == nil {
		t.Fatalf("expected error on oversized manifest, got nil")
	}
}

// ---------------------------------------------------------------------------
// parseFilename — <artifact>-<version>.jar fallback
// ---------------------------------------------------------------------------

func TestParseFilename(t *testing.T) {
	cases := []struct {
		in                 string
		wantArtifact, wantV string
	}{
		// Simple happy path.
		{"commons-lang3-3.12.0.jar", "commons-lang3", "3.12.0"},
		// Version with qualifier suffix.
		{"spring-boot-2.7.18-RELEASE.jar", "spring-boot", "2.7.18-RELEASE"},
		// No hyphen at all — whole base is the artifact, no version.
		{"tools.jar", "tools", ""},
		// Hyphen but no digit suffix — don't guess a version that isn't there.
		{"nothing-like-a-version.jar", "nothing-like-a-version", ""},
		// Multiple numeric segments; version splits at the last hyphen
		// whose suffix starts with a digit.
		{"my-project-utils-2.1.0.jar", "my-project-utils", "2.1.0"},
		// Snapshot convention — version includes -SNAPSHOT.
		{"lib-1.0-SNAPSHOT.jar", "lib", "1.0-SNAPSHOT"},
		// Uppercase doesn't matter for splitting.
		{"Foo-Bar-1.2.jar", "Foo-Bar", "1.2"},
		// Not a .jar — caller shouldn't hand this to us but be defensive.
		{"weird.txt", "", ""},
	}
	for _, tc := range cases {
		t.Run(tc.in, func(t *testing.T) {
			gotA, gotV := parseFilename(tc.in)
			if gotA != tc.wantArtifact || gotV != tc.wantV {
				t.Errorf("parseFilename(%q) = (%q, %q); want (%q, %q)",
					tc.in, gotA, gotV, tc.wantArtifact, tc.wantV)
			}
		})
	}
}

