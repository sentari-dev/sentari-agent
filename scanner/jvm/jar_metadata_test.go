func TestExtractFromJar_PomPropertiesWins(t *testing.T) {
	// When a JAR has pom.properties AND a MANIFEST.MF, pom.properties
	// takes precedence: it's the Maven-authoritative source.
	jar := buildJAR(t, map[string][]byte{
		"META-INF/maven/org.example/widget/pom.properties": []byte(
			"groupId=org.example\nartifactId=widget\nversion=1.0\n",
		),
		"META-INF/MANIFEST.MF": []byte(
			"Manifest-Version: 1.0\r\n" +
				"Implementation-Title: Widget\r\n" +
				"Implementation-Version: 999.0\r\n",
		),
	})

	records, errs := extractFromJar(jar)
	if len(errs) > 0 {
		t.Fatalf("unexpected errors: %+v", errs)
	}
	if len(records) != 1 {
		t.Fatalf("expected 1 record, got %d", len(records))
	}
	got := records[0]
	if got.Name != "org.example:widget" {
		t.Errorf("Name: got %q want org.example:widget", got.Name)
	}
	if got.Version != "1.0" {
		t.Errorf("Version: got %q want 1.0", got.Version)
	}
	if got.EnvType != EnvJVM {
		t.Errorf("EnvType: got %q want %q", got.EnvType, EnvJVM)
	}
}

func TestExtractFromJar_ShadedUberJarMultipleCoordinates(t *testing.T) {
	// An uber-jar bundles multiple Maven artefacts; each has its own
	// pom.properties under META-INF/maven/<group>/<artifact>/.  We must
	// emit one PackageRecord per embedded coordinate, not merge them.
	// This is the key differentiator vs manifest-only tools that see
	// the uber-jar as a single artefact.
	jar := buildJAR(t, map[string][]byte{
		"META-INF/maven/org.a/lib-a/pom.properties": []byte("groupId=org.a\nartifactId=lib-a\nversion=1.0\n"),
		"META-INF/maven/org.b/lib-b/pom.properties": []byte("groupId=org.b\nartifactId=lib-b\nversion=2.0\n"),
		"META-INF/maven/org.c/lib-c/pom.properties": []byte("groupId=org.c\nartifactId=lib-c\nversion=3.0\n"),
	})

	records, _ := extractFromJar(jar)
	if len(records) != 3 {
		t.Fatalf("expected 3 records (one per embedded coordinate), got %d", len(records))
	}
	names := map[string]string{}
	for _, r := range records {
		names[r.Name] = r.Version
	}
	if names["org.a:lib-a"] != "1.0" || names["org.b:lib-b"] != "2.0" || names["org.c:lib-c"] != "3.0" {
		t.Errorf("shaded coordinates not all extracted correctly: %+v", names)
	}
}

func TestExtractFromJar_ManifestFallbackWhenNoPomProperties(t *testing.T) {
	// Some JARs (notably OSGi bundles from the Eclipse p2 tooling) have
	// a MANIFEST.MF but no META-INF/maven directory.  We fall back to
	// OSGi Bundle-* headers; Implementation-* is the next fallback.
	jar := buildJAR(t, map[string][]byte{
		"META-INF/MANIFEST.MF": []byte(
			"Manifest-Version: 1.0\r\n" +
				"Bundle-SymbolicName: org.eclipse.core.runtime\r\n" +
				"Bundle-Version: 3.26.100\r\n",
		),
	})

	records, errs := extractFromJar(jar)
	if len(errs) > 0 {
		t.Fatalf("unexpected errors: %+v", errs)
	}
	if len(records) != 1 {
		t.Fatalf("expected 1 record, got %d", len(records))
	}
	if records[0].Name != "org.eclipse.core.runtime" {
		t.Errorf("Name: got %q want org.eclipse.core.runtime", records[0].Name)
	}
	if records[0].Version != "3.26.100" {
		t.Errorf("Version: got %q want 3.26.100", records[0].Version)
	}
}

func TestExtractFromJar_FilenameFallbackLastResort(t *testing.T) {
	// No pom.properties, no MANIFEST.MF — the filename is the last
	// resort.  We still emit a record (otherwise the CVE-correlation
	// layer misses this library entirely) but the caller can detect
	// the heuristic source if they care (future: add a Source field).
	jar := buildJAR(t, nil)
	// buildJAR wrote to <tmp>/fixture.jar; we can't rename cheaply
	// across filesystems, so verify the filename extractor independently
	// in parseFilename tests above — here just assert we get SOMETHING
	// rather than empty output.
	records, _ := extractFromJar(jar)
	if len(records) != 1 {
		t.Fatalf("expected 1 filename-fallback record, got %d", len(records))
	}
	if records[0].Name == "" {
		t.Errorf("expected non-empty Name from filename fallback")
	}
}

func TestExtractFromJar_CorruptedZip(t *testing.T) {
	// A corrupt/truncated JAR must produce a ScanError, not a panic.
	// Write a few bytes of garbage and pass that path.
	tmp := t.TempDir() + "/garbage.jar"
	if err := os.WriteFile(tmp, []byte("not-a-zip-file"), 0o644); err != nil {
		t.Fatalf("write garbage file: %v", err)
	}
	records, errs := extractFromJar(tmp)
	if len(errs) == 0 {
		t.Errorf("expected a ScanError for corrupt JAR")
	}
	if len(records) != 0 {
		t.Errorf("expected no records for corrupt JAR, got %d", len(records))
	}
}

func TestExtractFromJar_ZipSlipAttackIgnored(t *testing.T) {
	// A malicious JAR might name an entry "../../../etc/passwd" trying
	// to escape a naive extractor.  We don't extract — we only read
	// specific files by their logical path.  Verify the malicious
	// filename doesn't hand us bogus metadata from an unrelated host
	// file, and doesn't cause the parser to misbehave.
	jar := buildJAR(t, map[string][]byte{
		"../../../etc/passwd":                         []byte("root:x:0:0:root:/root:/bin/bash\n"),
		"META-INF/maven/o/a/pom.properties":           []byte("groupId=o\nartifactId=a\nversion=1\n"),
	})
	records, _ := extractFromJar(jar)
	// The pom.properties we planted must parse; the zip-slip entry
	// must be ignored by the iteration (we only look at entries whose
	// logical path matches our patterns).
	if len(records) != 1 || records[0].Name != "o:a" {
		t.Errorf("zip-slip attack may have corrupted extraction: %+v", records)
	}
}

